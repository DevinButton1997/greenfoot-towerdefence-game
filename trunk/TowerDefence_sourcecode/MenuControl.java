import greenfoot.*;  // (World, Actor, GreenfootImage, Greenfoot and MouseInfo)

/**
 * Write a description of class MenuSteuerung here.
 * 
 * @author (Kevin Huber) 
 * @version (1.0)
 */
public class MenuControl extends Actor
{
    public MenuControl()
    {
        setImage(new GreenfootImage(1, 1));
    }

    /**
     * Act - do whatever the MenuSteuerung wants to do. This method is called whenever
     * the 'Act' or 'Run' button gets pressed in the environment.
     */
    public void act() 
    {
        if( Greenfoot.mouseClicked(((myWorld) getWorld()).map1) )
        {
            ((myWorld) getWorld()).setLevel(1);
        }
        else if( Greenfoot.mouseClicked(((myWorld) getWorld()).map2) )
        {
            ((myWorld) getWorld()).setLevel(2);
        }
    }    
}
